# Practice Exercise 4_1 (functie met 3 parameters)
# Schrijf (en test) de functie som() die 3 parameters heeft: getal1, getal2
# en getal3. De return-waarde van de functie moet de som (optelling) van deze parameters zijn!

def som(getal1, getal2, getal3):
    optellen = getal1 + getal2 + getal3
    return optellen

print(som(1,2,3))
print(som(45,56,67))