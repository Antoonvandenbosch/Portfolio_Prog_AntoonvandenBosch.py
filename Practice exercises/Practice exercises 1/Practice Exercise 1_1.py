# Practice Exercise 1_1: Noteer van elke expressie wat de uitkomst en type is

a = 5               # Uitkomst is 5, type is integer
b = 5.0             # Uitkomst is 5.0, type is float
c = 5%2             # Uitkomst is 1, type is integer
d = 5>1             # Uitkomst is true, type is boolean
e = '5'             # Uitkomst is '5', type is string
f = 5*2             # Uitkomst is 10, type is integer
g = '5'*2           # Uitkomst is '55', type is string
h = 5/2             # Uitkomst is 2.5, type is float
i = 5//2            # Uitkomst is 2, type is integer
j= [5,2,1]          # Uitkomst is [5,2,1], type is list
k = 5 in [1, 4, 6]  # Uitkomst is false, type is boolean

print(a)
print(b)
print(c)
print(d)
print(e)
print(f)
print(g)
print(h)
print(i)
print(j)
